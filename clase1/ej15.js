/*
    Operadores de asignación y aritméticos

    Asigna un valor a el operador de la izquierda en función a el valor del
    operando de la derecha.
*/

// Asignación con incremento (+=)
var numero = 0;
console.log(numero += 1); // numero = numero + 1

var numero = 0;
console.log(numero += 1); // cumple la misma función que numero++

var numero = 0;
console.log(numero += 4); // numero = numero + 4. Incrementa en 4 el valor de numero

// Asignación Desincrementando (-=)
var numero = 10;
console.log(numero -= 1); // numero = numero - 1

var numero = 10;
console.log(numero -= 1); // cumple la misma función que numero--

var numero = 10;
console.log(numero -= 4); // numero = numero - 4. Desincrementa en 4 el valor de numero

// Multiplicacion
var numero = 10;
console.log( numero *= 4);

// División
var numero = 10;
console.log( numero /= 4);
