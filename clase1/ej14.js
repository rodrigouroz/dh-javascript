/*
    Operadores aritméticos:

    Toman valores númericos y sus operandos y devuelven un resultado numerico.
*/

var veinte = 20;
var cuatro = 4;

// Suma (+)
var resultado = veinte + cuatro;
console.log(resultado);
console.log( veinte + cuatro );

// Resta (-)
var resultado = veinte - cuatro;
console.log(resultado);
console.log( veinte - cuatro );

// Multiplicación (*)
var resultado = veinte * cuatro;
console.log(resultado);
console.log( veinte * cuatro );

// División (/)
var resultado = veinte / cuatro;
console.log(resultado);
console.log( veinte / cuatro );

// Módulo (%)
var resultado = veinte % cuatro;
console.log(resultado);
console.log( veinte % cuatro );


// Incremento en 1 (++)
var cuatro = 4;
console.log('cuatro++: ', cuatro++);
// falta mostrar
console.log('++cuatro: ', ++cuatro);
console.log(cuatro);

// Desincrementar en 1 (--)
var cuatro = 4;
console.log('cuatro--: ', cuatro--);
// falta uno
console.log('--cuatro: ', --cuatro);
console.log(cuatro);
